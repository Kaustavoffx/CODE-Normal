# Operation on List data

# List Creation
list1 = [1, 2, 3]
list2 = [4, 5, 6]

# List Concatenation
concatenatedList = list1 + list2
print("Concatenated List:", concatenatedList)

# List Repetition
repeatedList = list1 * 2
print("Repeated List:", repeatedList)

# List Membership
isPresent = 5 in concatenatedList
print("Is 5 in the concatenated list?:", isPresent)

# List Slicing
slicedList = concatenatedList[2:5]
print("Sliced List:", slicedList)