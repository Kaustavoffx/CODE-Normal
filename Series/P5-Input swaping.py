n1= float(input("Enter first number: "))
n2= float(input("Enter second number: "))

#swapping of two numbers
temp=n1
n1=n2
n2=temp
print(f"""After swapping: 
----------
First number: {n1}
Second number: {n2}""")


#output
# Enter first number: 88
# Enter second number: 77
# After swapping: 
# ----------
# First number: 77.0
# Second number: 88.0